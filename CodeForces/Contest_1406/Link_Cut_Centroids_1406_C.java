    public static void main(String[] args) throws IOException {
        FastIO in = new FastIO(args);
        int t = in.ni();
        while (t-- > 0) {
            int n = in.ni();
            int adja[][] = in.gg(n, n - 1);
            int subtree[] = new int[n + 1];
            int parent[] = new int[n + 1];
            Stack<Integer> st = new Stack<>();
            boolean b[] = new boolean[n + 1];
            st.push(1);
            LinkedList<Integer> min_max = new LinkedList<>();
            int min_maxx = n + 1;
            while (!st.isEmpty()) {
                int root = st.peek();
                if (b[root]) {
                    int max = 0;
                    for (int i : adja[root]) {
                        max = Math.max(max, subtree[i]);
                        subtree[root] += subtree[i];
                    }
                    max = Math.max(max, n - subtree[root] - 1);
                    if (min_maxx > max) {
                        min_maxx = max;
                        while (!min_max.isEmpty())
                            min_max.remove();
                    }
                    if (min_maxx == max) {
                        min_max.add(root);
                    }
                    subtree[root]++;
                    st.pop();
                } else {
                    b[root] = true;
                    for (int i : adja[root]) {
                        if (!b[i]) {
                            st.push(i);
                            parent[i] = root;
                        }
                    }
                }
            }
            if (min_max.size() == 1) {
                System.out.println("1 " + adja[1][0]);
                System.out.println("1 " + adja[1][0]);
            } else {
                int leaf = 0;
                for (int i = 1; i <= n; i++) {
                    if (adja[i].length == 1) {
                        leaf = i;
                    }
                }
                Arrays.fill(b, false);
                Arrays.fill(parent, 0);
                int n1 = min_max.remove();
                int n2 = min_max.remove();
                st.push(n1);
                while (!st.isEmpty()) {
                    int root = st.pop();
                    b[root] = true;
                    for (int i : adja[root]) {
                        if (!b[i]) {
                            st.push(i);
                            parent[i] = root;
                        }
                    }
                }
                System.out.println(leaf + " " + parent[leaf]);
                boolean g = false;
                int curr = leaf;
                while (parent[curr] != 0) {
                    if (parent[curr] == n2) {
                        g = true;
                    }
                    curr = parent[curr];
                }
                if (g) {
                    System.out.println(leaf + " " + n1);
                } else {
                    System.out.println(leaf + " " + n2);
                }
            }
        }
        in.bw.flush();
    }

    static class Data implements Comparable<Data> {

        int a, b;

        public Data(int a, int b) {
            this.a = a;
            this.b = b;
        }

        @Override
        public int compareTo(Data o) {
            if (a == o.a) {
                return Integer.compare(b, o.b);
            }
            return Integer.compare(a, o.a);
        }

        public static void sort(int a[]) {
            Data d[] = new Data[a.length];
            for (int i = 0; i < a.length; i++) {
                d[i] = new Data(a[i], 0);
            }
            Arrays.sort(d);
            for (int i = 0; i < a.length; i++) {
                a[i] = d[i].a;
            }
        }
    }

