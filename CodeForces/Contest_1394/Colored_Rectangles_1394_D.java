    public static void main(String[] args) throws IOException {
        FastIO in = new FastIO(args);
        int r = in.ni();
        int g = in.ni();
        int b = in.ni();
        int rr[] = in.gia(r);
        int gg[] = in.gia(g);
        int bb[] = in.gia(b);
        Arrays.sort(rr);
        Arrays.sort(gg);
        Arrays.sort(bb);
        long dp[][][] = new long[r + 1][g + 1][b + 1];
        long ans = 0;
        for (int i = r; i >= 0; i--) {
            for (int j = g; j >= 0; j--) {
                for (int k = b; k >= 0; k--) {
                    if (i < r && j < g) {
                        dp[i][j][k] = Math.max(dp[i][j][k], dp[i + 1][j + 1][k] + rr[i] * gg[j]);
                    }
                    if (k < b && j < g) {
                        dp[i][j][k] = Math.max(dp[i][j][k], dp[i][j + 1][k + 1] + bb[k] * gg[j]);
                    }
                    if (i < r && k < b) {
                        dp[i][j][k] = Math.max(dp[i][j][k], dp[i + 1][j][k + 1] + rr[i] * bb[k]);
                    }
                    ans = Math.max(ans, dp[i][j][k]);
                }
            }
        }
        System.out.println(ans);
        in.bw.flush();
    }

