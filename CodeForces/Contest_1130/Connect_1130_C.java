    private static void dfs(int x, int y, boolean b[][], LinkedList<Integer> l, char c[][]) {
        int n = b.length;
        if (x >= n || x < 0 || y >= n || y < 0) {
            return;
        }
        if (b[x][y]) {
            return;
        }
        b[x][y] = true;
        int count = 0;
        if (x + 1 < n && c[x + 1][y] == 0) {
            dfs(x + 1, y, b, l, c);
            count++;
        }
        if (y + 1 < n && c[x][y + 1] == 0) {
            dfs(x, y + 1, b, l, c);
            count++;
        }
        if (x - 1 >= 0 && c[x - 1][y] == 0) {
            dfs(x - 1, y, b, l, c);
            count++;
        }
        if (y - 1 >= 0 && c[x][y - 1] == 0) {
            dfs(x, y - 1, b, l, c);
            count++;
        }
        if (count != 4) {
            l.add(x * n + y);
        }
    }

    private static int dist(int x, int y, int z, int w) {
        return (int) (Math.pow(x - y, 2) + Math.pow(z - w, 2));
    }

